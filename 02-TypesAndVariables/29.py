import random


dice1 = random.randrange(1, 6) #Rolling the dice 1
print(f'The number you got is: {dice1}')
dice2 = random.randrange(1, 6) #Rolling the dice 2
print(f'The number you got is: {dice2}')
dice3 = random.randrange(1, 6) #Rolling the dice 3
print(f'The number you got is: {dice3}')
sum = dice1+dice2+dice3 #The sum of dices
print(f'The sum of dice rolled is: {sum}')
