#Put in the temperature in Celsius
celsius = float(input('Enter the temperature in Celsius: '))
#Calculating the temperature in Fahrenheit
fahrenheit = 2*celsius+32
#Calculatin the temperature in Kalvin
kalvin = celsius+273.15
#Displaying the temperatures
print(f'The temperature in Fahrenheit is: {fahrenheit}')
print(f'The temperature in Kalvin is: {kalvin}')